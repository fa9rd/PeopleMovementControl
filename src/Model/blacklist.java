package Model;

import java.sql.Date;

/**
 *
 * @author Shah
 */
public class blacklist {
    public int id;
    public String userId;
    public Date date;

    public blacklist() {
    }

    public blacklist(int id, String userId, Date date) {
        this.id = id;
        this.userId = userId;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
    
    
}
